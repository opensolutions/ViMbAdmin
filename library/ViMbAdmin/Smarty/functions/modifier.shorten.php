/*
 * Returns with a shortened string, with '...' appended, or with the input string if it was short enough.
 *
 * @param string $str
 * @param int $length
 * @return string
 * @package ViMbAdmin
 * @subpackage Smarty_Functions
 */
function smarty_modifier_shorten( $str, $length )
{
    if( mb_strlen( $str ) > $length )
        return rtrim( mb_substr( $str, 0, $length ) ) . '...';

    return $str;
}
